

function alternarDiv() {
    var div = document.getElementById('menuDiv');
    if (div.style.display === 'none') {
      div.style.display = 'block'; // Altera o estilo para 'block' para exibir a div
    } else {
      div.style.display = 'none'; // Altera o estilo para 'none' para esconder a div
    }
  }
  
